import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { compile } from 'json-schema-to-typescript'
import prettier from 'prettier'

const isCheck = process.argv.includes('--check')
const schemaNames = [
  'agent',
  'artifact',
  'approval',
  'blueprint',
  'capability',
  'error',
  'event',
  'incubation',
  'policy',
  'task'
]

const outputs = new Map([
  ['apps/desktop/shared/generated/contracts.ts', await generateTypeScript()],
  ['engine/internal/contracts/generated/contracts.go', generateGo()]
])

let changed = false
for (const [path, content] of outputs) {
  if (isCheck) {
    const current = readOptional(path)
    if (current !== content) {
      console.error(`${path} is not up to date. Run npm run specs:generate.`)
      changed = true
    }
    continue
  }

  mkdirSync(dirname(path), { recursive: true })
  writeFileSync(path, content)
  console.log(`generated ${path}`)
}

if (changed) {
  process.exit(1)
}

if (isCheck) {
  console.log('Generated contracts are up to date.')
}

async function generateTypeScript() {
  const prettierOptions =
    (await prettier.resolveConfig('apps/desktop/shared/generated/contracts.ts')) ?? {}
  const chunks = [
    '/* eslint-disable */',
    '// Generated by scripts/generate-contracts.mjs. Do not edit manually.',
    ''
  ]

  for (const name of schemaNames) {
    const schema = JSON.parse(readFileSync(join('specs', `${name}.schema.json`), 'utf8'))
    const compiled = await compile(schema, schema.title, {
      bannerComment: '',
      additionalProperties: false,
      enableConstEnums: false,
      format: false,
      style: {
        semi: false,
        singleQuote: true,
        trailingComma: 'none'
      }
    })
    chunks.push(compiled.trim(), '')
  }

  chunks.push(
    'export type RuntimePingResponse = RuntimePingSuccessResponse | RuntimePingFailureResponse',
    '',
    'export interface RuntimePingSuccessResponse {',
    "  schema_version: '0.1'",
    '  ok: true',
    '  engineVersion: string',
    '  trace_id: string',
    '}',
    '',
    'export interface RuntimePingFailureResponse {',
    "  schema_version: '0.1'",
    '  ok: false',
    '  error: DreamWorkerError',
    '  trace_id: string',
    '}',
    ''
  )

  return prettier.format(chunks.join('\n'), { ...prettierOptions, parser: 'typescript' })
}

function generateGo() {
  return `// Code generated by scripts/generate-contracts.mjs. DO NOT EDIT.
package generated

type DreamWorkerError struct {
\tCode        string \`json:"code"\`
\tMessage     string \`json:"message"\`
\tRecoverable bool   \`json:"recoverable"\`
\tUserAction  string \`json:"user_action"\`
\tTraceID     string \`json:"trace_id"\`
}

type RuntimePingResponse struct {
\tSchemaVersion string            \`json:"schema_version"\`
\tOK            bool              \`json:"ok"\`
\tEngineVersion string            \`json:"engineVersion,omitempty"\`
\tTraceID       string            \`json:"trace_id"\`
\tError         *DreamWorkerError \`json:"error,omitempty"\`
}

type EventEnvelope struct {
\tEventID       string         \`json:"event_id"\`
\tSchemaVersion string         \`json:"schema_version"\`
\tTraceID       string         \`json:"trace_id"\`
\tMissionID     string         \`json:"mission_id"\`
\tRunID         string         \`json:"run_id"\`
\tActor         string         \`json:"actor"\`
\tTimestamp     string         \`json:"timestamp"\`
\tType          string         \`json:"type"\`
\tPayload       map[string]any \`json:"payload"\`
}

type ArtifactMetadata struct {
\tSchemaVersion string \`json:"schema_version"\`
\tArtifactID    string \`json:"artifact_id"\`
\tMissionID     string \`json:"mission_id"\`
\tRunID         string \`json:"run_id,omitempty"\`
\tKind          string \`json:"kind"\`
\tTitle         string \`json:"title"\`
\tVersion       int    \`json:"version"\`
\tURI           string \`json:"uri"\`
\tContentType   string \`json:"content_type,omitempty"\`
}
`
}

function readOptional(path) {
  try {
    return readFileSync(path, 'utf8')
  } catch {
    return null
  }
}
